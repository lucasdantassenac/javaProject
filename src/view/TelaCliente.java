    public void readTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) tblCliente.getModel();
        tblCliente.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        ClienteDao clienteDao = new ClienteDao();
        for (Cliente cliente: clienteDao.read()){
            modelo.addRow(new Object[]{
                cliente.getId(),
                cliente.getNome(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getRg(),
                cliente.getCpf(),
                cliente.getEndereco(),
                cliente.getCep()
            });
        }
    }
     public void readTableBusca(String busca) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) tblCliente.getModel();
        tblCliente.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        ClienteDao clienteDao = new ClienteDao();
        for (Cliente cliente: clienteDao.readBusca(busca)){
            modelo.addRow(new Object[]{
                cliente.getId(),
                cliente.getNome(),
                cliente.getTelefone(),
                cliente.getEmail(),
                cliente.getRg(),
                cliente.getCpf(),
                cliente.getEndereco(),
                cliente.getCep()
            });
        }
    }
