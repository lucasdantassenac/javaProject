    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
         if(tblLivros.getSelectedRow() != -1){
            
            Livros livro = new Livros();
            LivroDao sDao = new LivroDao();
            
            livro.setTitulo(txtTitle.getText());
            livro.setPageNumber(Integer.parseInt(txtPNumber.getText()));
            livro.setId((int) tblLivros.getValueAt(tblLivros.getSelectedRow(), 0));
            
            try {
                sDao.update(livro);
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                limpar();
            } catch (SQLException ex) {
                Logger.getLogger(Principal.class.getName()).log(Level.SEVERE, null, ex);
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "clique num registro");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

