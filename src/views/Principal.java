    public void readTable() throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        tblLivros.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        LivroDao livroDao = new LivroDao();
        for (Livros livro: livroDao.read()){
            modelo.addRow(new Object[]{
                livro.getId(),
                livro.getTitulo(),
                livro.getPageNumber()
            });
        }
    }
     public void readTableBusca(String busca) throws SQLException{
        DefaultTableModel modelo = (DefaultTableModel) tblLivros.getModel();
        tblLivros.setRowSorter(new TableRowSorter(modelo));
        modelo.setNumRows(0);
        LivroDao livroDao = new LivroDao();
        for (Livros livro: livroDao.readBusca(busca)){
            modelo.addRow(new Object[]{
                livro.getId(),
                livro.getTitulo(),
                livro.getPageNumber()
            });
        }
    }
